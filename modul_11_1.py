# 1. Requests
# Описание: requests — это простая и удобная библиотека для выполнения HTTP-запросов в Python.

import requests

# Запрос данных с сайта
url = 'https://api.github.com'
response = requests.get(url)

# Вывод статуса запроса
print(f"Status Code: {response.status_code}")

# Вывод данных в формате JSON
data = response.json()
print(data)

# Комментарий:
# requests позволяет легко выполнять HTTP-запросы и работать с ответами.
# Это значительно упрощает взаимодействие с веб-сервисами и API.
# Основные функции включают get, post, put, delete и другие методы для выполнения различных типов запросов.


# 2. Pandas
# Описание: pandas — это мощная библиотека для анализа данных и манипуляции с ними.

import pandas as pd

# Чтение данных из CSV файла
df = pd.read_csv('data.csv')

print(df.head())

# Простой анализ данных: вычисление среднего значения каждого столбца
mean_values = df.mean()
print(mean_values)

# Простой анализ данных: подсчет количества пропущенных значений
missing_values = df.isnull().sum()
print(missing_values)

# Комментарий:
# pandas предоставляет мощные инструменты для работы с данными, такие как чтение и запись данных в различных форматах,
# анализ данных, обработка пропущенных значений и многое другое. Основные функции включают read_csv,
# DataFrame, mean, isnull и другие методы для анализа данных.

# 3. NumPy
# Описание: numpy — это библиотека для работы с массивами и математическими функциями.

import numpy as np

# Создание массива чисел
array = np.array([1, 2, 3, 4, 5])

# Выполнение математических операций с массивом
squared_array = np.square(array)
print(squared_array)

# Вычисление среднего значения массива
mean_value = np.mean(array)
print(mean_value)

# Вычисление суммы элементов массива
sum_value = np.sum(array)
print(sum_value)

# Комментарий:
# numpy предоставляет мощные инструменты для работы с массивами и выполнения математических операций.
# Это значительно упрощает работу с числовыми данными и позволяет выполнять сложные вычисления эффективно.
# Основные функции включают array, square, mean, sum и другие методы для работы с массивами.


